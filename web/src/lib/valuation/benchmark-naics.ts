function normalizeNaics(naics: string | undefined): string {
  return (naics ?? "").replace(/\D/g, "");
}

function seedForNaics(naics: string | undefined): { key: string; seed: BenchmarkSeed } {
  const normalized = normalizeNaics(naics);
  if (normalized && BENCHMARKS[normalized]) return { key: normalized, seed: BENCHMARKS[normalized]! };
  if (normalized.slice(0, 2) && BENCHMARKS[normalized.slice(0, 2)]) {
    return { key: normalized.slice(0, 2), seed: BENCHMARKS[normalized.slice(0, 2)]! };
  }
  return { key: normalized || "default", seed: DEFAULT_INDUSTRY };
}

export function buildNaicsBenchmarkProfile(naics?: string): NaicsBenchmarkProfile {
  const { key, seed } = seedForNaics(naics);
  const benchmarkRows: BenchmarkEntryRow[] = ROW_MAP.map(({ section, label, key: ratioKey }) => ({
    section,
    label,
    value: seed.ratios[ratioKey] ?? "",
    source: seed.ratios[ratioKey] ? "industry-common-size" : "na",
    formula: seed.ratios[ratioKey] ? `IRS SOI / Census CBP benchmark (${key})` : undefined,
  }));

  return {
    naics: normalizeNaics(naics) || key,
    title: seed.title,
    benchmarkRows,
    narrative: seed.narrative,
    sources: [IRS_SOI_SOURCE],
  };
}
